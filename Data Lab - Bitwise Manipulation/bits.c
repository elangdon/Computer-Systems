/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2 
 */
int leastBitPos(int x) {
	//The ~x+1 allows us to carry the +1 until we get to the figure which was 
	//a 0 in the complement --This gives us the position of the LSB
  
	return x&(~x+1);
}
/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
	//If either proposition of the or statement returns true, the value is an ASCII digit.
	//This works by filtering out the second byte if it is 0x3. It then stores whether or
	//not the value is less than 7.  if the second byte is 0x3 and the first byte <=7, it 
	//will return false.  Then it checks is the last byte is 8 or 9. If either of these return
	//false, then the return statement is true since we ! each proposition.

	return !((x ^ (0x30)) & (~(0x7))) | !((x ^ (0x30)) & (~(0x9)));
}
/*
 * satMul2 - multiplies by 2, saturating to Tmin or Tmax if overflow
 *   Examples: satMul2(0x30000000) = 0x60000000
 *             satMul2(0x40000000) = 0x7FFFFFFF (saturate to TMax)
 *             satMul2(0x60000000) = 0x80000000 (saturate to TMin)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
